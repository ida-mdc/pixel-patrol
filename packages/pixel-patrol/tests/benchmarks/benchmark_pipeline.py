# Stream + loader helpers
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

def _null_stream(
    n_files: int,
    shape:   tuple,
    dtype:   Any,
    n_images: int,
    prefix:  str = "/null/bench",
) -> Iterator[Tuple[Path, dict]]:
    size_bytes = int(np.prod(shape)) * np.dtype(dtype).itemsize * max(1, n_images)
    for i in range(n_files):
        p = f"{prefix}/f_{i:05d}.npy"
        yield Path(p), {
            "path":              p,
            "name":              f"f_{i:05d}.npy",
            "size_bytes":        size_bytes,
            "file_extension":    "npy",
            "modification_date": datetime(2026, 1, 1),
            "common_base":       Path(prefix).name or prefix,
        }


def _null_loader(sc: Dict[str, Any]) -> NullLoader:
    entries = {
        f"/null/bench/f_{i:05d}.npy": (sc["shape"], sc["dtype"], sc["dim_order"], sc["n_images"])
        for i in range(sc["n_files"])
    }
    return NullLoader(entries)


def _count_tasks(sc: Dict[str, Any], mode: str, datasets_dir: Path) -> Dict[str, Any]:
    config = ProcessingConfig(**sc["config_kw"])
    if mode == "memory":
        loader = _null_loader(sc)
        stream = _null_stream(sc["n_files"], sc["shape"], sc["dtype"], sc["n_images"])
    else:
        loader = NumpyLoader()
        stream = _discover_files([datasets_dir / sc["name"]], "all")
    dummy_meta: List[dict] = []
    counts: Dict[str, int] = {}
    for t in _plan_tasks(stream, config, loader, dummy_meta):
        k = _TASK_TYPE_NAMES.get(type(t), type(t).__name__)
        counts[k] = counts.get(k, 0) + 1
    return {"n_tasks": sum(counts.values()), "task_types": counts}


def _rss_mb(proc: psutil.Process) -> float:
    return proc.memory_info().rss / 1024 / 1024


# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# Single run
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

def run_one(
    sc:           Dict[str, Any],
    mode:         str,
    n_workers:    int,
    client:       Client,
    datasets_dir: Path,
    task_info:    Dict[str, Any],
    processors:   Optional[List[Any]] = None,
) -> Dict[str, Any]:
    if processors is None:
        processors = PROCESSORS
    config = ProcessingConfig(**sc["config_kw"])
    if mode == "memory":
        loader = _null_loader(sc)
        stream = _null_stream(sc["n_files"], sc["shape"], sc["dtype"], sc["n_images"])
    else:
        loader = NumpyLoader()
        stream = _discover_files([datasets_dir / sc["name"]], "all")

    files_meta: List[dict] = []
    task_stream = _plan_tasks(stream, config, loader, files_meta)

    parts_dir: Optional[Path] = None
    if sc.get("use_parts_dir"):
        parts_dir = Path(tempfile.mkdtemp(prefix="bench_parts_"))

    coord_proc = psutil.Process()
    _get_worker_rss = lambda: psutil.Process().memory_info().rss  # noqa: E731
    worker_rss_before = client.run(_get_worker_rss)

    n_records_done = [0]

    def _count_progress(done: int, total: int) -> None:
        n_records_done[0] = done

    t_total = time.perf_counter()
    _, pipeline_stats = _coordinate_pipeline(
        client=client,
        task_stream=task_stream,
        files_meta=files_meta,
        loader=loader,
        processors=processors,
        config=config,
        parts_dir=parts_dir,
        on_progress=_count_progress,
        n_workers=n_workers,
        is_distributed=False,
    )
    wall_total = time.perf_counter() - t_total

    if parts_dir is not None:
        shutil.rmtree(parts_dir, ignore_errors=True)

    coord_rss = _rss_mb(coord_proc)
    worker_rss_after = client.run(_get_worker_rss)
    worker_rss_deltas = [
        (worker_rss_after[w] - worker_rss_before.get(w, 0)) / 1024 / 1024
        for w in worker_rss_after
    ]

    total_data_mb = (
        int(np.prod(sc["shape"]))
        * np.dtype(sc["dtype"]).itemsize
        * max(1, sc["n_images"])
        * sc["n_files"]
    ) / 1024 / 1024

    n_records = n_records_done[0]
    return {
        "scenario": {
            "name":              sc["name"],
            "desc":              sc["desc"],
            "n_files":           sc["n_files"],
            "n_images_per_file": sc["n_images"],
            "image_shape":       list(sc["shape"]),
            "dim_order":         "".join(sc["dim_order"]),
            "dtype":             np.dtype(sc["dtype"]).name,
            "total_data_mb":     round(total_data_mb, 2),
            "mb_per_task":       sc["config_kw"].get("mb_per_task"),
            "slice_size":  sc["config_kw"].get("slice_size"),
        },
        "run": {
            "mode":      mode,
            "n_workers": n_workers,
        },
        "tasks": {
            "n_tasks":       task_info["n_tasks"],
            "task_types":    task_info["task_types"],
            "n_records_out": n_records,
        },
        "timing_s": {
            "wall_total": round(wall_total, 4),
        },
        "memory_mb": {
            "coord_rss":        round(coord_rss, 1),
            "worker_rss_max":   round(max((r / 1024 / 1024 for r in worker_rss_after.values()), default=0.0), 1),
            "worker_rss_avg":   round(sum(r / 1024 / 1024 for r in worker_rss_after.values()) / max(len(worker_rss_after), 1), 1),
            "worker_delta_max": round(max(worker_rss_deltas, default=0.0), 1),
        },
        "throughput": {
            "data_mb_per_s": round(total_data_mb / wall_total, 2) if wall_total > 0 else None,
            "records_per_s": round(n_records / wall_total, 1) if wall_total > 0 else None,
        },
        "speedup_vs_1w": None,
    }


# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# Output
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

_HDR = (
    f"{'Scenario':<22} {'Mode':<6} {'W':>2}  "
    f"{'WallTotal':>9}  "
    f"{'Rec':>6} {'Tasks':>6} {'MB/s':>6}  {'Spdup':>5}"
)
_SEP = "─" * len(_HDR)


def _fmt_s(v: float) -> str:
    return f"{v:7.2f}s" if v >= 0.005 else f"{v*1000:6.0f}ms"


def _print_row(r: dict) -> None:
    t  = r["timing_s"]
    ru = r["run"]
    tk = r["tasks"]
    th = r["throughput"]
    spdup = f"{r['speedup_vs_1w']:.2f}x" if r.get("speedup_vs_1w") else "   -  "
    mbs   = f"{th['data_mb_per_s']:5.1f}" if th.get("data_mb_per_s") else "   - "
    print(
        f"{r['scenario']['name']:<22} {ru['mode']:<6} {ru['n_workers']:>2}  "
        f"{_fmt_s(t['wall_total'])}  "
        f"{tk['n_records_out']:>6} {tk['n_tasks']:>6} {mbs:>6}  {spdup:>5}"
    )


def _print_table(rows: List[dict]) -> None:
    print()
    print(_HDR)
    prev_scenario = None
    for r in rows:
        name = r["scenario"]["name"]
        if name != prev_scenario:
            print(_SEP)
            prev_scenario = name
        _print_row(r)
    print(_SEP)


# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# Main
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

def main() -> None:
    parser = argparse.ArgumentParser(
        description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter
    )
    parser.add_argument("--scenarios", nargs="+", help="Scenario names to run (default: all)")
    parser.add_argument("--workers",   nargs="+", type=int,
                        help="Worker counts for LocalCluster (default: 1 2 4). Ignored with --scheduler.")
    parser.add_argument("--scheduler", type=str, default=None,
                        help="Connect to an existing Dask scheduler (e.g. tcp://host:8786). "
                             "Runs all scenarios once against the remote cluster.")
    parser.add_argument("--skip-file", action="store_true", help="Skip from-file runs")
    args = parser.parse_args()

    bench_dir    = Path(__file__).parent
    datasets_dir = bench_dir / "datasets"
    results_dir  = bench_dir / "results"
    results_dir.mkdir(exist_ok=True)

    selected  = {sc["name"] for sc in SCENARIOS} if not args.scenarios else set(args.scenarios)
    scenarios = [sc for sc in SCENARIOS if sc["name"] in selected]

    for i, sc in enumerate(scenarios):
        if "file" in sc["modes"] and not args.skip_file:
            d = datasets_dir / sc["name"]
            if not d.exists() or not any(d.glob("*.npy")):
                print(f"[warn] dataset missing for '{sc['name']}' - skipping file mode.")
                scenarios[i] = {**sc, "modes": tuple(m for m in sc["modes"] if m != "file")}

    all_results: List[dict] = []

    def _run_scenarios(client: Client, n_workers: int) -> None:
        for sc in scenarios:
            modes = [m for m in sc["modes"] if not (args.skip_file and m == "file")]
            if not modes:
                continue
            task_info_by_mode: Dict[str, Dict] = {}
            for mode in modes:
                try:
                    task_info_by_mode[mode] = _count_tasks(sc, mode, datasets_dir)
                except Exception:
                    task_info_by_mode[mode] = {"n_tasks": -1, "task_types": {}}
            for mode in modes:
                print(f"  running {sc['name']} [{mode}, w={n_workers}] …", end="", flush=True)
                try:
                    result = run_one(sc, mode, n_workers, client, datasets_dir,
                                     task_info_by_mode[mode], processors=sc.get("processors"))
                    all_results.append(result)
                    print(f"  {result['timing_s']['wall_total']:.2f}s")
                except Exception as exc:
                    print(f"  ERROR: {exc}")

    if args.scheduler:
        print(f"\nConnecting to scheduler at {args.scheduler} …")
        with Client(args.scheduler) as client:
            n_workers = sum(client.nthreads().values())
            print(f"  {n_workers} worker(s) available")
            _run_scenarios(client, n_workers)
    else:
        worker_counts: List[int] = sorted(set(args.workers or [1, 2, 4]))
        for n_workers in worker_counts:
            print(f"\n{'━'*60}\n  LocalCluster: {n_workers} worker(s)\n{'━'*60}")
            with LocalCluster(
                n_workers=n_workers, threads_per_worker=1, processes=True, silence_logs=40
            ) as cluster, Client(cluster) as client:
                print(f"  Dashboard → {cluster.dashboard_link}")
                client.submit(lambda: None, pure=False).result()
                _run_scenarios(client, n_workers)

    baseline: Dict[Tuple[str, str], float] = {
        (r["scenario"]["name"], r["run"]["mode"]): r["timing_s"]["wall_total"]
        for r in all_results if r["run"]["n_workers"] == 1
    }
    for r in all_results:
        b = baseline.get((r["scenario"]["name"], r["run"]["mode"]))
        r["speedup_vs_1w"] = round(b / r["timing_s"]["wall_total"], 3) if b else None

    _print_table(all_results)

    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    out_path = results_dir / f"pipeline_{ts}.json"
    with open(out_path, "w") as f:
        json.dump(all_results, f, indent=2, default=str)
    print(f"\nResults saved → {out_path}")


if __name__ == "__main__":
    main()
