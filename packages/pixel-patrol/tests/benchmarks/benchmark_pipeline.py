class _MeanStd:
    NAME          = "bench"
    CHUNK_KIND    = ChunkKind.LEAF
    OUTPUT        = "features"
    OUTPUT_SCHEMA = {"mean": float, "std": float}

    class INPUT:
        axes = None
        kinds = None
        capabilities = None
        kind_patterns = None

    def run_chunk(self, record: Record) -> Dict[str, Any]:
        d = record.data
        return {"mean": float(np.mean(d)), "std": float(np.std(d))}

    def get_aggregation(self, col: str):
        return lambda rows, g_dims: float(np.mean([r[col] for r in rows if col in r]))


PROCESSORS = [_MeanStd()]


