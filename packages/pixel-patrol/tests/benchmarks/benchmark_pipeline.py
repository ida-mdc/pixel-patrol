"""
Pipeline benchmark - measures Dask processing pipeline throughput and scaling.

Usage:
    cd packages/pixel-patrol/tests/benchmarks
    python benchmark_pipeline.py                           # run all scenarios
    python benchmark_pipeline.py --scenarios tiny_batch many_small
    python benchmark_pipeline.py --workers 1 4
    python benchmark_pipeline.py --skip-file               # memory-mode only

From-file runs require datasets generated by make_datasets.py first.

Results are saved to results/pipeline_<timestamp>.json.

JSON structure per entry:
  scenario     - n_files, shape, dtype, total_data_mb, config
  run          - mode (memory|file), n_workers
  tasks        - n_tasks, task_types breakdown, n_records_out
  timing_s     - wall_total
  memory_mb    - coord_rss, worker_rss_max/avg, worker_delta_max
  throughput   - data_mb_per_s, records_per_s
  speedup_vs_1w
"""
